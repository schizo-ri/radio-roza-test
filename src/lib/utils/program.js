const sortedByDay = program.sort((a, b) => a.day.localeCompare(b.day));

// get program on a weekday
function getProgramOnWeekday(day) {
  return program.filter((show) => show.day === day);
}

function getCurrentShow() {
  const now = new Date();
  const day = now.toLocaleDateString('en-US', { weekday: 'long' });

  const todayProgram = getProgramOnWeekday(day);
  if (!todayProgram?.length) {
    return null;
  }

  // Get all shows that have already started, then find the most recent one
  const startedShows = todayProgram
    .filter((show) => new Date(show.full_date) <= now)
    .sort((a, b) => new Date(b.full_date) - new Date(a.full_date));

  return startedShows[0] || null;
}

function getNextShow() {
  const now = new Date();

  // Find all upcoming shows across all days, not just today
  const upcomingShows = program
    .filter((show) => new Date(show.full_date) > now)
    .sort((a, b) => new Date(a.full_date) - new Date(b.full_date));

  return upcomingShows[0] || null;
}

export { program, sortedByDay, getProgramOnWeekday, getCurrentShow, getNextShow };
