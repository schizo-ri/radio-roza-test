<h4>Kratki opis projekta:</h4>
<ProjectParagraph>
  Radijska emisija "17 bitnih" okuplja 17 voditelja koji kroz 17 epizoda razgovaraju s gostima o
  ciljevima održivog razvoja. Emisija se emitira svakog utorka i petka točno u podne na Radio Roži.
</ProjectParagraph>
<ProjectParagraph>
  Nisu svi čuli za ciljeve održivog razvoja i zato se udruga Ri Rock prihvatila mikrofona, okupila
  17 mladih voditelja i putem on-line streaminga odlučila ući u domove diljem hrvatske.
</ProjectParagraph>
<ProjectParagraph>
  Prije pet godina <strong> Ujedinjeni narodi </strong> su usvojili i svijetu predstavili
  <strong>17 ciljeva održivog razvoja </strong> sa željom da se oni postignu do 2030. godine. Hrvatska
  je kao jedna od članica UN-a aktivna u ostvarivanju ovih ciljeva od početka, a osnovano je i Nacionalno
  vijeće za održivi razvoj koje predlaže mjere, aktivnosti i prioritete te prati, analizira i koordinira
  njihovu provedbu. Evo koji su to ciljevi:
</ProjectParagraph>
<ul>
  <li>1. Iskorjenjivanje siromaštva (iskorijeniti siromaštvo svuda i u svim oblicima)</li>
  <li>
    2. Iskorjenjivanje gladi (iskorijeniti glad, postići sigurnost hrane i poboljšanu ishranu te
    promovirati održivu poljoprivredu)
  </li>
  <li>
    3. Zdravlje i dobrobit (osigurati zdrav život i promovirati blagostanje za ljude svih
    generacija)
  </li>
  <li>
    4. Kvalitetno obrazovanje (osigurati uključivo i kvalitetno obrazovanje te promovirati
    mogućnosti cjeloživotnog učenja)
  </li>
  <li>5. Rodna ravnopravnost (postići rodnu ravnopravnost i osnažiti sve žene i djevojke)</li>
  <li>
    6. Pitka voda i higijenski uvjeti (osigurati pristup pitkoj vodi za sve, održivo upravljati
    vodama te osigurati higijenske uvjete za sve)
  </li>
  <li>
    7. Pristupačna i čista energija (osigurati pristup pouzdanoj, održivoj i suvremenoj energiji po
    pristupačnim cijenama za sve)
  </li>
  <li>
    8. Dostojanstven rad i gospodarski rast (promovirati uključiv i održiv gospodarski rast, punu
    zaposlenost i dostojanstven rad za sve)
  </li>
  <li>
    9. Industrija, inovacije i infrastruktura (izgraditi prilagodljivu infrastrukturu, promovirati
    uključivu i održivu industrijalizaciju i poticati inovativnost)
  </li>
  <li>10. Smanjiti nejednakosti (smanjiti nejednakost unutar i između država)</li>
  <li>
    11. Održivi gradovi i održive zajednice (učiniti gradove i naselja uključivim, sigurnim,
    prilagodljivim i održivim
  </li>
  <li>12. Odgovorna potrošnja i proizvodnja (osigurati održive oblike potrošnje i proizvodnje)</li>
  <li>
    13. Odgovor na klimatske promjene (poduzeti hitne akcije u borbi protiv klimatskih promjena i
    njihovih posljedice)
  </li>
  <li>
    14. Život ispod vode (očuvati i održivo koristiti oceane, mora i morske resurse za održiv
    razvoj)
  </li>
  <li>
    15. Život na kopnu (zaštititi, uspostaviti i promovirati održivo korištenje kopnenih ekosustava,
    održivo upravljati šumama, suzbiti dezertifikaciju, zaustaviti degradaciju tla te spriječiti
    uništavanje biološke raznolikosti)
  </li>
  <li>
    16. Mir, pravda i snažne institucije (promovirati miroljubiva i uključiva društva za održivi
    razvoj, osigurati pristup pravdi za sve i izgraditi učinkovite, odgovorne i uključive
    institucije na svim razinama)
  </li>
  <li>
    17. Partnerstvo za ciljeve (ojačati načine provedbe te učvrstiti globalno partnerstvo za održivi
    razvoj)
  </li>
</ul>
<ProjectParagraph>
  Radio Roža je radio zajednice (community radio) nastao iz želje i potrebe za praćenjem nezavisne
  kulturne scene i raznih kulturnih aktera u Rijeci, promocijom najrazličitijih (underground)
  žanrova, te ljubavi prema gramofonskim pločama.
</ProjectParagraph>
